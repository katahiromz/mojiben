KAKIJUN g_kanji3_kakijun;

#define ADD_LINEAR(angle, data) do { \
    ga.type     = LINEAR; \
    ga.angle0   = angle; \
    ga.res      = (100 + j) * 100 + i; \
    vga.push_back(ga); \
    ++i; \
} while (0)

#define ADD_POLAR(a0, a1, center_x, center_y, data) do { \
    ga.type     = POLAR; \
    ga.angle0   = a0; \
    ga.angle1   = a1; \
    ga.cx       = center_x; \
    ga.cy       = center_y; \
    ga.res      = (100 + j) * 100 + i; \
    vga.push_back(ga); \
    ++i; \
} while (0)

#define ADD_WAIT() do { \
    ga.type     = WAIT; \
    vga.push_back(ga); \
} while (0)

#define NEXT_MOJI() do { \
    g_kanji3_kakijun[index++] = vga; \
    vga.clear(); \
    ++j; i = 0; \
} while (0)

